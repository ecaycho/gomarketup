package qreasymarket.modelo;

public class UsuarioRol {
	private int IdUsuarioRol;
	private String myRol;
	private int myUsuario;

	public int getIdUsuarioRol() {
		return IdUsuarioRol;
	}

	public void setIdUsuarioRol(int idUsuarioRol) {
		IdUsuarioRol = idUsuarioRol;
	}

	public String getMyRol() {
		return myRol;
	}

	public void setMyRol(String myRol) {
		this.myRol = myRol;
	}

	public int getMyUsuario() {
		return myUsuario;
	}

	public void setMyUsuario(int myUsuario) {
		this.myUsuario = myUsuario;
	}

}
